package model;

public class VolEquipage {
    private int id;
    private int vol_id;
    private int equipage_id;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getVol_id() {
        return vol_id;
    }

    public void setVol_id(int vol_id) {
        this.vol_id = vol_id;
    }

    public int getEquipage_id() {
        return equipage_id;
    }

    public void setEquipage_id(int equipage_id) {
        this.equipage_id = equipage_id;
    }

}
